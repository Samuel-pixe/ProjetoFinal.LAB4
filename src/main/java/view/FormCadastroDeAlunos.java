    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Aluno obj = new Aluno();
        Curso cat = (Curso)cbxCurso.getSelectedItem();
        Aluno cat2 = (Aluno)cbxSemestre.getSelectedItem();
        obj.setId_curso(cat.getId());
        obj.setNome(txtNome_aluno.getText());
        obj.setEmail(txtEmail.getText());
        obj.setEndereco(txtEndereco.getText());
        obj.setEndereco(txtCidade.getText());
        obj.setTelefone(txtTelefone.getText());
        obj.setUf(txtUf.getText());
        obj.setCpf(txtCpf.getText());
        obj.setSemestre(cat2.getSemestre());
        
        
        //Executar a operação 
        AlunoDAO dao = new AlunoDAO();
        int resultado;
        
        //se o campo Id estiver vazio
        if (txtCod.getText().isEmpty()){
            //inserir 
            resultado = dao.inserir(obj);
        }else{
            //atualizar
            obj.setCod(Integer.parseInt(txtCod.getText()));
            resultado = dao.atualizar(obj);
        }
            
        //int resultado = dao.inserir(obj);
        
        if (resultado == 1){
            JOptionPane.showMessageDialog(
                null,
                "Operação realizada com sucesso!"
            );
        }else{
            JOptionPane.showMessageDialog(
                null,
                "Ocorreu um erro.",
                "Loja",
                JOptionPane.ERROR_MESSAGE
            );
        }
        this.dispose();// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed


