private void preencherComboCurso(){
        //recuperar a lista de categorias
        List<Curso> lista = new CursoDAO().listar();
        
        if (lista != null){
            DefaultComboBoxModel m = new DefaultComboBoxModel();
            for(Curso obj : lista){
                m.addElement(obj); //{id,nome,fg_ativo}
            }
            cbxCurso.setModel(m);
        }
    }
